public class MusicPlayer {
    public void Play(){
        System.out.println("您点击了播放按钮！音乐播放器开始播放。。。。");
    }

    public void Pause(){
        System.out.println("您点击了暂停按钮！音乐播放器已经暂停。。。。");
    }

    public void Nextward(){
        System.out.println("您点击了下一首！音乐播已调到下一首歌曲。。。。");
    }

    public void Upward(){
        System.out.println("您点击了上一首！音乐播已回到上一首歌曲。。。。");
    }
}
